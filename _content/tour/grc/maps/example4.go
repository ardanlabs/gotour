// Δείγμα προγράμματος, προκειμένου να παρουσιαστεί πως δηλώνεται, λαμβάνει αρχική
// τιμή και γίνεται επαναληπτική προσπέλαση, σε ένα σχεσιακό πίνακα. Παρουσιάζεται,
// επίσης, πως η επαναληπτική προσπέλαση ενός σχεσιακού πίνακα είναι τυχαία.
package main

import "fmt"

// Ο user αναπαριστά κάποιον, που χρησιμοποιεί το πρόγραμμα.
type user struct {
	name    string
	surname string
}

func main() {

	// Δηλώστε και δώστε αρχικές τιμές στον σχεσιακό πίνακα.
	users := map[string]user{
		"Roy":     {"Rob", "Roy"},
		"Ford":    {"Henry", "Ford"},
		"Mouse":   {"Mickey", "Mouse"},
		"Jackson": {"Michael", "Jackson"},
	}

	// Προσπελάστε επαναληπτικά τον σχεσιακό πίνακα,
	// τυπώνοντας κάθε κλειδί και τιμή.
	for key, value := range users {
		fmt.Println(key, value)
	}

	fmt.Println()

	// Προσπελάστε επαναληπτικά τον σχεσιακό πίνακα, τυπώνοντας,
	// απλά, τα κλειδιά. Παρατηρείστε, ότι τα αποτελέσματα είναι
	// διαφορετικά.
	for key := range users {
		fmt.Println(key)
	}
}
